export function getProductsByCategory(category: string): Product[] {
  return products.filter((p) => p.category === category)
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.badge)
}

export function getDealsOfTheDay(): Product[] {
  return products.filter((p) => p.originalPrice)
}

export function getBrands(): string[] {
  const brands = [...new Set(products.map((p) => p.brand))]
  return brands.sort()
}

export function getBrandsByCategory(category: string): string[] {
  const filtered = category ? products.filter((p) => p.category === category) : products
  const brands = [...new Set(filtered.map((p) => p.brand))]
  return brands.sort()
}

export function getSizesByCategory(category: string): string[] {
  const filtered = category ? products.filter((p) => p.category === category) : products
  const sizes = [...new Set(filtered.map((p) => p.size).filter(Boolean) as string[])]
  return sizes
}

export function formatPrice(price: number): string {
  return `KES ${price.toLocaleString("en-KE")}`
}
